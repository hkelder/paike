package ee.kg.paike.saaremaapaike;

class Event {

    String day;
    String date;
    String heading;
    String location;
    String category;

    Event(String day, String date, String heading, String location, String category) {

        this.day = day;
        this.date = date;
        this.heading = heading;
        this.location = location;
        this.category = category;
    }
}
